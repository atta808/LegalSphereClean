/**
 * Resolves a safe country entry, falling back cleanly to DEFAULT when not found.
 * @param {string} countryCode - ISO Alpha-2 Country Code (e.g. 'PK', 'US')
 * @returns {Object} Full structural data profile
 */
const safeResolveCountry = (countryCode) => {
  const normalizedKey = String(countryCode || "").toUpperCase();
  return LEGAL_SYSTEMS[normalizedKey] || LEGAL_SYSTEMS.DEFAULT;
};

/**
 * Resolves properties safely for both legacy structures and extended metadata wrappers.
 * V1 relied on direct root extraction (e.g., node.countryName). V2 wraps this in .metadata.
 * This function cleanly services both formats to safeguard legacy component parsing.
 *
 * @param {string} countryCode - ISO Alpha-2 code
 * @returns {Object} Standardized platform-wide object block
 */
export const getLegalSystemByCountry = (countryCode) => {
  const target = safeResolveCountry(countryCode);
  return {
    ...target,
    countryName: target.metadata
      ? target.metadata.countryName
      : target.countryName,
    legalSystemType: target.metadata
      ? target.metadata.legalSystemType
      : undefined,
    defaultLanguage: target.metadata
      ? target.metadata.defaultLanguage
      : undefined,
    defaultCurrency: target.metadata
      ? target.metadata.defaultCurrency
      : undefined,
  };
};

/**
 * Returns case management tool arrays filtering out custom configuration runtimes.
 */
export const getCMSByCountry = (countryCode) => {
  const system = safeResolveCountry(countryCode);
  return Array.isArray(system.cms)
    ? system.cms.filter((item) => item.isEnabled !== false)
    : [];
};

/**
 * Returns data tool resources assigned to the context lookup sequence.
 */
export const getResearchToolsByCountry = (countryCode) => {
  const system = safeResolveCountry(countryCode);
  return Array.isArray(system.research)
    ? system.research.filter((item) => item.isEnabled !== false)
    : [];
};

/**
 * Safely extracts items for any of the 13 modular system engine categories.
 *
 * @param {string} countryCode - ISO country format code
 * @param {string} categoryName - Category field key name (e.g. 'barCouncils', 'legislation')
 * @returns {Array} Array containing standard item objects
 */
export const getCategoryToolsByCountry = (countryCode, categoryName) => {
  const system = safeResolveCountry(countryCode);
  const cluster = system[categoryName];
  return Array.isArray(cluster)
    ? cluster.filter((item) => item.isEnabled !== false)
    : [];
};

